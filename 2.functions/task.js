function getArrayParams(...arr) {
  let min = Infinity;
  let max = -Infinity;
  let sum = 0;

  for (let i = 0; i < arr.length; i++) {
    const current = arr[i];
    max = Math.max(max, current);
    min = Math.min(min, current);
    sum += current;
  }

  const avg = +(sum / arr.length).toFixed(2);
  
  return { min: min, max: max, avg: avg };
}

